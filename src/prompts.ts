// ============ 函数：生成随机提示词 ============
export function generatePrompt(addOptimize: boolean = ADD_OPTIMIZE): string {
  // Core subject (always first and explicit)
  const subject = randomPick(subjectDescriptions, 1)[0];
  const ageDesc = randomPick(ageAppearance, 1)[0];
  // Core portrait element - ensures female portrait context
  const coreElement = randomPick(corePortraitElements, 1)[0];

  // Hair
  const selectedHair = randomPick(hair, 1)[0];

  // Outfit
  const selectedOutfit = randomPick(outfit, 2).join('，');

  // Accessories
  const selectedAccessories = randomPick(accessories, 2).join('，');

  // Props - 50% chance to include props
  const selectedProps = Math.random() > 0.5 ? randomPick(props, 2).join('，') : '';

  // Face & Body
  const selectedFace = randomPick(face, 2).join('，');
  const selectedBody = randomPick(body, 2).join('，');

  // Makeup
  const selectedMakeup = randomPick(makeup, 1)[0];

  // Hand gestures & Eye expressions
  const selectedHandGesture = randomPick(handGestures, 1)[0];
  const selectedEyeExpression = randomPick(eyeExpressions, 1)[0];

  // Pose
  const selectedPose = randomPick(pose, 2).join('，');

  // Background
  const selectedBackground = randomPick(background, 1)[0];

  // Lighting
  const selectedLighting = randomPick(lighting, 1)[0];

  // Color palettes & textures
  const selectedColor = randomPick(colorPalettes, 1)[0];
  const selectedTexture = Math.random() > 0.5 ? randomPick(textures, 1)[0] : '';

  // Era styles & Mood
  const selectedEra = Math.random() > 0.6 ? randomPick(eraStyles, 1)[0] : '';
  const selectedMood = randomPick(moodReferences, 1)[0];

  // Mood
  const selectedVibe = randomPick(vibe, 1)[0];

  // Photography style & Artist style
  const selectedStyle = randomPick(photographyStyle, 1)[0];
  const selectedArtist = Math.random() > 0.5 ? randomPick(artistStyles, 1)[0] : '';

  // Camera angles
  const selectedCameraAngle = randomPick(cameraAngles, 1)[0];

  // Composition
  const selectedComposition = randomPick(composition, 1)[0];

  // Quality
  const selectedQuality = randomPick(quality, 1)[0];

  // Build prompt with subject first - CRITICAL elements stay in place
  // Subject and core element are ALWAYS first (not shuffled)
  const criticalParts: string[] = [
    subject,
    ageDesc,
    selectedHair,
    coreElement,  // Always include core portrait element
  ];

  // Other elements that can be shuffled
  const shuffleParts: string[] = [
    selectedOutfit,
    selectedAccessories,
    selectedFace,
    selectedBody,
    selectedMakeup,
    selectedHandGesture,
    selectedEyeExpression,
    selectedPose,
    selectedBackground,
    selectedLighting,
    selectedColor,
    selectedVibe,
    selectedStyle,
    selectedCameraAngle,
    selectedComposition,
    selectedQuality,
  ];

  // Add optional elements to shuffle pool
  if (selectedProps) shuffleParts.push(selectedProps);
  if (selectedTexture) shuffleParts.push(selectedTexture);
  if (selectedEra) shuffleParts.push(selectedEra);
  if (selectedMood) shuffleParts.push(selectedMood);
  if (selectedArtist) shuffleParts.push(selectedArtist);

  // Shuffle middle parts
  const shuffledMiddle = shuffleParts.sort(() => Math.random() - 0.5);

  // Final: critical parts first, then shuffled
  const finalParts = [...criticalParts, ...shuffledMiddle];

  const promptText = finalParts.join('，');

  // Add --optimize if configured
  return addOptimize ? `${promptText} --optimize` : promptText;
}

// Generate multiple prompts
export function generatePrompts(count: number, addOptimize: boolean = ADD_OPTIMIZE): string[] {
  return Array.from({ length: count }, () => generatePrompt(addOptimize));
}

